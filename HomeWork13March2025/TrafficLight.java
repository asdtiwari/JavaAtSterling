// Traffic Light conditional statement program
public class TrafficLight {
	public static void main(String[] args) {
		int input = 1;
		if(input == 1) { // for red
			System.out.println("Stop");
		} else if(input == 2) { // for yellow
			System.out.println("Ready");
		} else { // for green
			System.out.println("Go");
		}
	}
}
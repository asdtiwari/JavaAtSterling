public class HouseColorDress {
	public static void main(String[] args) {
		// 0 for red; 1 for blue; 2 for green; 3 for yellow
		int color = 0;
		if(color == 0) {
			System.out.println("Tagore House");
		}
		else if(color == 1) {
			System.out.println("Shastri House");
		}
		else if(color == 2) {
			System.out.println("Nehru House");
		}
		else if(color == 3) {
			System.out.println("Gandhi House");
		}
		else {
			System.out.println("Invalid Color");
		}
	}
}
/* Write a program to print the following pattern: 
Expected Output: 
* * * * *  
* * * * *  
* * * * *  
* * * * *  
* * * * * 
*/
public class StarPrintPatterSquare {
	public static void main(String[] args) {
		int end = 5;
		for(int loop = 0; loop < end; ++loop) {
			System.out.println("* * * * *");
		}
	}
}
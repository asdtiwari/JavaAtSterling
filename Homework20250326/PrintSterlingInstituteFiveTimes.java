/* Write a program to print you name five times on console.
Expected Output:
Sterling Institute
Sterling Institute
Sterling Institute
Sterling Institute
Sterling Institute
*/
public class PrintSterlingInstituteFiveTimes {
	public static void main(String[] args) {
		int end = 5;
		for(int loop = 0; loop < end; ++loop) {
			System.out.println("Sterling Institute");
		}
	}
}
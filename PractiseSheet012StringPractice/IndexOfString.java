/* Write a progarm to check the index of "programming" in String "java is a programming language and it is easy to learn" */
public class IndexOfString {
	public static void main(String[] args) {
		String str = "Java is a programming language and it is easy to learn";
		System.out.println("In String: " + str + "\n" + "Index of programming is " + str.indexOf("programming"));
	}
}
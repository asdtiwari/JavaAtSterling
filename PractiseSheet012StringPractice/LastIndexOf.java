/* Write a program to check the last index of y in String "Java is a programming language and it is easy to learn" */
public class LastIndexOf {
	public static void main(String[] args) {
		String str = "Java is a programming language and it is easy to learn";
		System.out.println("In String: " + str + "\n" + "Last Index of y is " + str.lastIndexOf('y'));
	}
}
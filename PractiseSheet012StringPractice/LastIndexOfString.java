/* WAP to check the last index of "easy" in String "Java is a programming language and it is easy to learn" */
public class LastIndexOfString {
	public static void main(String[] args) {
		String str = "Java is a programming language and it is easy to learn";
		System.out.println("In String: " + str + "\n" + "Last Index of easy is " + str.lastIndexOf("easy"));
	}
}
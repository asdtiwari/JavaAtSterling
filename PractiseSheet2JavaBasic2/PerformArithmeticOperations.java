/* Write a program to enter two numbers and perform all arithmetic operations */

public class PerformArithmeticOperations {
	public static void main(String[] args) {
		int a = 10;
		int b = 5;
		System.out.println(
a + " + " + b + " = " + (a + b) + "\n" +
a + " - " + b + " = " + (a - b) + "\n" +
a + " * " + b + " = " + (a * b) + "\n" +
a + " / " + b + " = " + (a / b) + "\n" +
a + " % " + b + " = " + (a % b) + "\n");
	}
}
/* Write a Java program to display the following pattern
Sample Pattern:
   J     a     v     v     a
   J    a a     v   v     a a
J  J   aaaaa     v v     aaaaa
 JJ   a     a     v     a     a
*/

public class PrintPatternJava {
	public static void main(String[] args) {
		System.out.println(
"   J     a     v     v     a" + "\n" +
"   J    a a     v   v     a a" + "\n" +
"J  J   aaaaa     v v     aaaaa" + "\n" +
" JJ   a     a     v     a     a");
	}
}
public class IsVowelOrNot {
	public static void main(String[] args) {
		char inp = 'b'; // small case only
		if(inp == 'a') {
			System.out.println(inp + " is vowel");
		}
		else if(inp == 'e') {
			System.out.println(inp + " is vowel");
		}
		else if(inp == 'i') {
			System.out.println(inp + " is vowel");
		}
		else if(inp == 'o') {
			System.out.println(inp + " is vowel");
		}
		else if(inp == 'u') {
			System.out.println(inp + " is vowel");
		}
		else {
			System.out.println(inp + " is consonant");
		}
	}
}
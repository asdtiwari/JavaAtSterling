public class IsNeighbourOfIndia {
	public static void main(String[] args) {
		// 1 China, 2 Afghanistan, 3 Pakistan, 4 Sri Lanka, 5 Bangladesh, 6 Myanmar, 7 Bhutan, 8 Nepal
		int inp = 8;
		if(inp == 1) {
			System.out.println("China is India's neighbour");
		}
		else if(inp == 2) {
			System.out.println("Afghanistan is India's neighbour");
		} 
		else if(inp == 3) {
			System.out.println("Pakistan is India's neighbour");
		}
		else if(inp == 4) {
			System.out.println("Sri Lanka is India's neighbour");
		}
		else if(inp == 5) {
			System.out.println("Bangladesh is India's neighbour");
		}
		else if(inp == 6) {
			System.out.println("Myanmar is India's neighbour");
		}
		else if(inp == 7) {
			System.out.println("Bhutan is India's neighbour");
		}
		else if(inp == 8) {
			System.out.println("Nepal is India's neighbour");
		}
		else {
			System.out.println("Not a India's neighbour");
		}
	}
}